
abstract class LibraryItems {
	private int ID;
	private String title;
	private int year;


 LibraryItems (int ID, String title, int year){
	 this.ID = ID;
	 this.title = title;
	 this.year = year;
 	}
 
 int getID () {
		return ID;
	}
 
 String getTitle () {
		return title;
	}
 
 int getYear () {
	 return year;
 	}
 
 
 }

